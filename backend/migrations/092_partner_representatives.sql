CREATE TABLE IF NOT EXISTS partner_representatives (
  id            INT AUTO_INCREMENT PRIMARY KEY,
  type          ENUM('country','bundesland') NOT NULL DEFAULT 'country',
  code          VARCHAR(10)  NOT NULL COMMENT 'ISO 3166-1 alpha-2 oder DE-BW etc.',
  name_de       VARCHAR(100) NOT NULL,
  name_en       VARCHAR(100) NOT NULL,
  status        ENUM('free','occupied','pending') NOT NULL DEFAULT 'free',
  rep_name      VARCHAR(150) NULL COMMENT 'Name des Reprﾃ､sentanten',
  rep_email     VARCHAR(150) NULL,
  rep_website   VARCHAR(255) NULL,
  flag_emoji    VARCHAR(10)  NULL,
  notes         TEXT         NULL,
  sort_order    INT          NOT NULL DEFAULT 0,
  created_at    DATETIME     NOT NULL DEFAULT NOW(),
  updated_at    DATETIME     NOT NULL DEFAULT NOW() ON UPDATE NOW(),
  UNIQUE KEY uq_partner_code_type (code, type)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS partner_documents (
  id            INT AUTO_INCREMENT PRIMARY KEY,
  name_de       VARCHAR(200) NOT NULL,
  name_en       VARCHAR(200) NOT NULL,
  description_de TEXT        NULL,
  description_en TEXT        NULL,
  filename      VARCHAR(255) NOT NULL,
  file_path     VARCHAR(500) NOT NULL,
  file_size     INT          NULL,
  mime_type     VARCHAR(100) NULL,
  category      ENUM('info','contract','application','other') NOT NULL DEFAULT 'info',
  is_public     TINYINT(1)   NOT NULL DEFAULT 1,
  sort_order    INT          NOT NULL DEFAULT 0,
  created_at    DATETIME     NOT NULL DEFAULT NOW()
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

